#!/usr/bin/env bash

[ -n "${_INC_BANNER+x}" ] && return
typeset -xr _INC_BANNER

#####################################################################
function banner() {
#####################################################################
    local -r message=$1 ; : ${message:?<- missing argument in "'${FUNCNAME[0]}()'"}

    echo -e "\n\n*********************************************************************"
    echo -e "*** ${message}"
    echo -e "*********************************************************************\n"
}

export -f banner
